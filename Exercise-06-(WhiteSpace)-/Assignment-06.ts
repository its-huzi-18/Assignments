// Exercise-06-(WhiteSpace)

// Store a person's name with whitespace
const personNameWithWhitespace: string = "\t   Huzaifa \n";

// Print the name with whitespace
console.log(`Name with whitespace: "${personNameWithWhitespace}"`);

// Strip whitespace from the name
const strippedName: string = personNameWithWhitespace.trim();

// Print the name after stripping whitespace
console.log(`Stripped Name: "${strippedName}"`);
